int	ft_atoi(char *str)
{
	int	res;

	res = 0;
	while (*str == ' ')
		str++;
	if (!(*str >= '0' && *str <= '9') && *str != 0)
		return (-1);
	while (*str >= '0' && *str <= '9')
		res = res * 10 + (*(str++) - 48);
	if (!(*str >= '0' && *str <= '9') && *str != 0)
		return (-1);
	return (res);
}
